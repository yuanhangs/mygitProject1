package class69.no13;



public class Shebei {
    public int bh;
    public String name;
    public int lx;
    public int rq;

    public Shebei(int bh, String name, int lx, int rq) {
        this.bh = bh;
        this.name = name;
        this.lx = lx;
        this.rq = rq;
    }

    public int getBh() {
        return bh;
    }

    public void setBh(int bh) {
        this.bh = bh;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLx() {
        return lx;
    }

    public void setLx(int lx) {
        this.lx = lx;
    }

    public int getRq() {
        return rq;
    }

    public void setRq(int rq) {
        this.rq = rq;
    }
}
