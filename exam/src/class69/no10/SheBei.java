package class69.no10;


public class SheBei {
	int id;
	String name;
	String lei;
	String day;
	

	public SheBei(int id, String name, String lei, String day) {
		super();
		this.id = id;
		this.name = name;
		this.lei = lei;
		this.day = day;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLei() {
		return lei;
	}
	public void setLei(String lei) {
		this.lei = lei;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	
}
